package com.br.uepb.model;

public class RelatorioModel {

	private int certa;
	private int errada;
	private int pulo;
	
	public int getCerta() {
		return certa;
	}
	public void setCerta(int certa) {
		this.certa = certa;
	}
	public int getErrada() {
		return errada;
	}
	public void setErrada(int errada) {
		this.errada = errada;
	}
	public int getPulo() {
		return pulo;
	}
	public void setPulo(int pulo) {
		this.pulo = pulo;
	}
	
	
}
